// @lc code=start
/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */
/**
 * @param {ListNode} head
 * @return {number}
 */
var getDecimalValue = function(head) {
    var result = 0;
    while (head) {
        result = result * 2 + head.val;
        head = head.next;
    };
    return result;
};
